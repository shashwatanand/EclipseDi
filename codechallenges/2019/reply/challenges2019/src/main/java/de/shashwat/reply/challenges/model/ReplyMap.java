package de.shashwat.reply.challenges.model;

public class ReplyMap {
	private int N;
	private int M;
	private int C;
	private int R;
	/**
	 * @param n
	 * @param m
	 * @param c
	 * @param r
	 */
	public ReplyMap(int n, int m, int c, int r) {
		super();
		N = n;
		M = m;
		C = c;
		R = r;
	}
	/**
	 * @return the n
	 */
	public int getN() {
		return N;
	}
	/**
	 * @param n the n to set
	 */
	public void setN(int n) {
		N = n;
	}
	/**
	 * @return the m
	 */
	public int getM() {
		return M;
	}
	/**
	 * @param m the m to set
	 */
	public void setM(int m) {
		M = m;
	}
	/**
	 * @return the c
	 */
	public int getC() {
		return C;
	}
	/**
	 * @param c the c to set
	 */
	public void setC(int c) {
		C = c;
	}
	/**
	 * @return the r
	 */
	public int getR() {
		return R;
	}
	/**
	 * @param r the r to set
	 */
	public void setR(int r) {
		R = r;
	}
	
	
}
